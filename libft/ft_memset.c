#include <string.h>

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*_b;

	i = 0;
	_b = b;
	while (i < len)
	{
		_b[i] = c;
		i++;
	}
	return (_b);
}
